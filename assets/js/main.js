let currentLang = localStorage.getItem('clinicFlowLang') || 'en';

function updateContent() {
    const d = content[currentLang];
    document.documentElement.dir = d.dir;
    document.documentElement.lang = currentLang;

    // Common Elements
    const langBtnText = document.getElementById('langBtnText');
    if (langBtnText) {
        langBtnText.innerText = d.langBtn;
        if (d.langBtn === "العربية") {
            langBtnText.classList.add('arabic-text');
        } else {
            langBtnText.classList.remove('arabic-text');
        }
    }

    const navBtn = document.getElementById('navBtn');
    if(navBtn) navBtn.innerText = d.navBtn;

    const foot = document.getElementById('foot');
    if(foot) foot.innerText = d.foot;

    const fL1 = document.getElementById('fL1'), fL2 = document.getElementById('fL2'), fL3 = document.getElementById('fL3');
    if(fL1) fL1.innerText = d.fL1;
    if(fL2) fL2.innerText = d.fL2;
    if(fL3) fL3.innerText = d.fL3;

    // Index Page Elements (Check if exist to avoid errors on other pages)
    if (document.getElementById('heroH1')) {
        document.getElementById('heroH1').innerText = d.heroH1;
        document.getElementById('heroP').innerText = d.heroP;
        document.getElementById('dlTextMain').innerText = d.dlTextMain;
        document.getElementById('dlTextSub').innerText = d.dlTextSub;
        document.getElementById('f1T').innerText = d.f1T; document.getElementById('f1D').innerText = d.f1D;
        document.getElementById('f2T').innerText = d.f2T; document.getElementById('f2D').innerText = d.f2D;
        document.getElementById('f3T').innerText = d.f3T; document.getElementById('f3D').innerText = d.f3D;
        document.getElementById('f4T').innerText = d.f4T; document.getElementById('f4D').innerText = d.f4D;
        document.getElementById('secH').innerText = d.secH;
        document.getElementById('s1T').innerText = d.s1T; document.getElementById('s1D').innerText = d.s1D;
        document.getElementById('s2T').innerText = d.s2T; document.getElementById('s2D').innerText = d.s2D;
        document.getElementById('s3T').innerText = d.s3T; document.getElementById('s3D').innerText = d.s3D;
        document.getElementById('s4T').innerText = d.s4T; document.getElementById('s4D').innerText = d.s4D;
        document.getElementById('reqH').innerText = d.reqH;
        document.getElementById('testiP').innerText = d.testiP;
        document.getElementById('platH').innerText = d.platH;
        document.getElementById('p1T').innerText = d.p1T; document.getElementById('p1D').innerText = d.p1D;
        document.getElementById('mainDlContent').style.textAlign = (currentLang === 'ar') ? 'right' : 'left';
        
        // Video Switching
        const video = document.getElementById('heroVideo');
        if (video) {
             // Only switch if source is different to avoid reloading if already set correctly on load
            const currentSrc = video.getAttribute('src');
            // Check if we need to update source (simple check)
            if (!currentSrc.endsWith(d.videoSrc.split('/').pop())) {
                 video.src = d.videoSrc;
                 video.load();
                 video.play().catch(e => console.error("Video play failed:", e));
            }
        }
    }

    // Privacy Page
    if (document.getElementById('privH1')) {
        document.getElementById('privH1').innerText = d.privH1;
        document.getElementById('privIntro').innerText = d.privIntro;
        document.getElementById('privSec1H').innerText = d.privSec1H;
        document.getElementById('privSec1P').innerText = d.privSec1P;
        document.getElementById('privSec2H').innerText = d.privSec2H;
        document.getElementById('privSec2P').innerText = d.privSec2P;
    }

    // Terms Page
    if (document.getElementById('termH1')) {
        document.getElementById('termH1').innerText = d.termH1;
        document.getElementById('termIntro').innerText = d.termIntro;
        document.getElementById('termSec1H').innerText = d.termSec1H;
        document.getElementById('termSec1P').innerText = d.termSec1P;
        document.getElementById('termSec2H').innerText = d.termSec2H;
        document.getElementById('termSec2P').innerText = d.termSec2P;
    }

    // Support Page
    if (document.getElementById('suppH1')) {
        document.getElementById('suppH1').innerText = d.suppH1;
        document.getElementById('suppIntro').innerText = d.suppIntro;
        document.getElementById('suppNameLabel').innerText = d.suppName;
        document.getElementById('suppEmailLabel').innerText = d.suppEmail;
        document.getElementById('suppMsgLabel').innerText = d.suppMsg;
        document.getElementById('suppBtn').innerText = d.suppBtn;
        
        document.getElementById('contactForm').style.textAlign = (currentLang === 'ar') ? 'right' : 'left';
    }
}

function toggleLanguage() {
    currentLang = currentLang === 'en' ? 'ar' : 'en';
    localStorage.setItem('clinicFlowLang', currentLang);
    updateContent();
}

// Initial Load
document.addEventListener('DOMContentLoaded', updateContent);
